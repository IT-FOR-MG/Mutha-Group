package com.muthagroup.vo;

public class MRMOperation_vo {

	private String parameter, year, passyear, passcomp;
	private int paraId;

	public String getParameter() {
		return parameter;
	}

	public void setParameter(String parameter) {
		this.parameter = parameter;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public int getParaId() {
		return paraId;
	}

	public void setParaId(int paraId) {
		this.paraId = paraId;
	}

	public String getPassyear() {
		return passyear;
	}

	public void setPassyear(String passyear) {
		this.passyear = passyear;
	}

	public String getPasscomp() {
		return passcomp;
	}

	public void setPasscomp(String passcomp) {
		this.passcomp = passcomp;
	}

}
